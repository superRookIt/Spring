package com.javalec.ex;

public class Event {

	int num;
	String title;
	String s_date;
	String e_date;

	public void e_print() {
		
		System.out.println(num+" "+title+" "+s_date+" "+e_date);

	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getS_date() {
		return s_date;
	}

	public void setS_date(String s_date) {
		this.s_date = s_date;
	}

	public String getE_date() {
		return e_date;
	}

	public void setE_date(String e_date) {
		this.e_date = e_date;
	}

}
